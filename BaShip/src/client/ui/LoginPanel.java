    /**
     * Compares two char[]
     * @return true of they have the same chars in the same order and false otherwise
     */
    private boolean compareCharArray(char[] ca1, char[] ca2)
    {
        if(ca1.length != ca2.length)
            return false;
        for(int i = 0; i < ca1.length ; i++)
            if(ca1[i]!=ca2[i])
                return false;
        return true;
    }
    
